def current_language() -> LanguageCode:
    """Return the active UI language."""

    return _current_language


def set_language(language: str) -> LanguageCode:
    """Set and return the active UI language."""

    global _current_language
    if language not in _TRANSLATIONS:
        raise ValueError(f"Unsupported language: {language!r}")
    _current_language = language  # type: ignore[assignment]
    return _current_language


def tr(key: str, **values: object) -> str:
    """Translate one key with English fallback and optional format values."""

    catalog = _TRANSLATIONS[_current_language]
    template = catalog.get(key, _TRANSLATIONS["en"].get(key, key))
    return template.format(**values) if values else template


def category_label(category: str) -> str:
    """Return a localized label for a stable asset-category identifier."""

    return tr(f"category.{category}")


